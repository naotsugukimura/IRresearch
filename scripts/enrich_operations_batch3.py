def enrich(filename: str, data: dict):
    path = DATA_DIR / filename
    with open(path, "r", encoding="utf-8") as f:
        j = json.load(f)

    j["operationsStory"]["dailySchedule"] = data["dailySchedule"]
    j["operationsStory"]["roles"] = data["roles"]
    j["operationsStory"]["typicalConversations"] = data["typicalConversations"]
    j["operationsStory"]["stakeholders"] = data["stakeholders"]

    with open(path, "w", encoding="utf-8") as f:
        json.dump(j, f, ensure_ascii=False, indent=2)
    print(f"  OK: {filename}")


