import json, os
BASE = os.path.join(os.path.dirname(__file__), '..', 'data', 'facility-analysis')

def inject(filename, maps):
    path = os.path.join(BASE, filename)
    with open(path, 'r', encoding='utf-8') as f:
        d = json.load(f)
    d.pop('serviceBlueprint', None)
    d['detailedProcessMaps'] = maps
    with open(path, 'w', encoding='utf-8') as f:
        json.dump(d, f, ensure_ascii=False, indent=2)
    print(f'OK: {filename} -> {len(maps)} maps')

